#!/bin/bash
echo "to perform the free , du , df , uname , ps , top , ping and call any 4 functions"

freeCommand () {
		echo "               RAM details                  "
		echo "============================================"
		free -m
		}

diskUtilization() {
		echo "               Disk Utilizatiom             "
                echo "============================================"
		du -h		
		}

diskFree() {
                echo "               Disk Available               "
                echo "============================================"
                df -h | head -15
                }

unameAll() {
                echo "                 VM Details                 "
                echo "============================================"
                uname -a
                }

topCommand() {
                echo "               top command                  "
                echo "============================================"
                top -n 5
                }
pingCommand() {
		echo "              Pinging google.com            "
		echo "============================================"
		ping google.com -c 10
}
psCommand() {
		echo "              PS command                    "
		echo "============================================"
		ps
}
		

freeCommand
echo "********************************************************************************************************************************"
diskUtilization
echo "******************************************************************************************************************************"
diskFree
echo "***************************************************************************************************************************************"
unameAll
echo "****************************************************************************************************************************************"
psCommand
echo "*****************************************************************************************************************************************"

